product_list = [
    (1, {'название': 'кофемашина', 'цена': '10000', 'количество': '7', 'единицы': 'шт.'}),
    (2, {'название': 'микроволновка', 'цена': '5000', 'количество': '4', 'единицы': 'шт.'}),
    (3, {'название': 'холодильник', 'цена': '15000', 'количество': '8', 'единицы': 'шт.'})
]

list = {}
for num, prod in product_list:
    for key, val in prod.items():
        if not list.get(key):
            list[key] = [val]
        else:
            list[key].append(val)

